package utils;

public class Variables {
	public static void main(String args[]){
		// 数值类型
		int intVariable = 0;
		long longVariable = 9l;
		double doubleVariable = 9.9;
		float floatVariable = 9.9f;
		// 布尔类型
		boolean booleanVariale = true;
		// 数组
		int[] arrayVariable = new int[10];
		// 字符串
		String stringVariable = new String("字符串");
		// char
		char charVariable = 'C';
		// print logs
		System.out.println(intVariable + longVariable + doubleVariable + floatVariable);
		System.out.println(charVariable);
		System.out.println("" + booleanVariale + arrayVariable + stringVariable + "");
		
	}

}
